const NAV_ITEMS = [
  { path: '/dashboard', label: 'Dashboard', icon: Icons.Dashboard },
  { path: '/candidates', label: 'Candidates', icon: Icons.Users },
  { path: '/search', label: 'Search', icon: Icons.Search, roles: ['super_admin', 'recruiter'] },
  { path: '/jobs', label: 'Jobs', icon: Icons.Briefcase, roles: ['super_admin', 'recruiter'] },
  { path: '/calendar', label: 'Calendar', icon: Icons.Calendar },
  { path: '/templates', label: 'WhatsApp', icon: Icons.MessageSquare, roles: ['super_admin', 'recruiter'] },
  { path: '/settings', label: 'Settings', icon: Icons.Settings, roles: ['super_admin'] }
];

export default function MainLayout() {
  const { user, signOut, hasRole } = useAuth();
  const navigate = useNavigate();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const handleSignOut = async () => {
    await signOut();
    navigate('/login');
  };

  const filteredNavItems = NAV_ITEMS.filter(item => 
    !item.roles || item.roles.some(role => hasRole(role))
  );

  return (
    <div className="main-layout">
      {/* Mobile Header */}
      <header className="mobile-header">
        <button className="menu-btn" onClick={() => setSidebarOpen(true)}>
          <Icons.Menu />
        </button>
        <span className="logo">Allied HR</span>
        <div className="user-avatar">
          {user?.displayName?.[0] || user?.email?.[0] || '?'}
        </div>
      </header>

      {/* Sidebar Overlay */}
      {sidebarOpen && (
        <div className="sidebar-overlay" onClick={() => setSidebarOpen(false)} />
      )}

      {/* Sidebar */}
      <aside className={`sidebar ${sidebarOpen ? 'open' : ''}`}>
        <div className="sidebar-header">
          <span className="logo">Allied HR</span>
          <button className="close-btn" onClick={() => setSidebarOpen(false)}>
            <Icons.X />
          </button>
        </div>

        <nav className="sidebar-nav">
          {filteredNavItems.map(item => (
            <NavLink
              key={item.path}
              to={item.path}
              className={({ isActive }) => `nav-item ${isActive ? 'active' : ''}`}
              onClick={() => setSidebarOpen(false)}
            >
              <item.icon />
              <span>{item.label}</span>
            </NavLink>
          ))}
        </nav>

        <div className="sidebar-footer">
          <div className="user-info">
            <div className="user-avatar-lg">
              {user?.displayName?.[0] || user?.email?.[0] || '?'}
            </div>
            <div className="user-details">
              <span className="user-name">{user?.displayName || 'User'}</span>
              <span className="user-role">{user?.role?.replace('_', ' ')}</span>
            </div>
          </div>
          <button className="logout-btn" onClick={handleSignOut}>
            <Icons.LogOut />
            <span>Sign Out</span>
          </button>
        </div>
      </aside>

      {/* Main Content */}
      <main className="main-content">
        <Outlet />
      </main>
    </div>
  );
}
